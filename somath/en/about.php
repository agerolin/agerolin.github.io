				  <h2>About: Signs of Maths<div style="float:right;"><a href="about_fi.html"><img src="../images/iconinfinnish"  alt=""/></a></div><br></h2>
                  <p>The Signs of Mathematics is an Outreach Science exhibition hosted by the University of Jyväskylä and developed mostly by researchers of both Department of Mathematics and Statistics and Department of Teacher Education. <br><br>

The main goals are to make accessible to the public some modern aspects of mathematical research and to <b>build bridges</b> between the university, schools, and society via promoting a wider understanding of mathematics. <br><br>

Through our devices we create a traversal path for learning not only important mathematical concepts studied at upper secondary school, bachelor, master, and research level, but also to discuss topics which traditionally are not studied in Bachelors and Master degrees in Finland. <br><br>  

Moreover, the website hosts multi-media content and follow-up discussions which can be used, by both teachers and students, as a starting point for self-studying or for developing related activities at classroom. <br><br>

Researchers, teachers, students, and everyone interested in mathematics and science are welcome to collaborate with us. Do not hesitate to <a href="contact.html">contact us</a> and discuss ideas with the members of our team. </p>
