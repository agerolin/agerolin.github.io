					<h2>Elliptical Billiard:
					  <div style="float:right;"><a href="#finnish"><img src="../../images/iconinfinnish"  alt=""/></a></div><br>
					  Game rules</h2>
                  <p class="para"> We describe a game called <em>"loop"</em> that is played on a billiard table of the shape of an ellipse.<br><br>                     
                    <p class="para">An ellipse (represented by the border of the table) is determined by its two focus points. 
<!-- More precisely, each point on the ellipse has exactly the same  sum of the distances from the two focus points. -->
In an elliptical billiard, one of the focus points is the pocket, and the other is marked by a small dark spot on the table.<br><br>

                  <h3> How to play it </h3>

<p class="para"> A game of loop is a 2-player game starting with 3 balls (red, yellow, and black) and a white cue ball.</p><br><br>
<img src="../../images/start.jpeg" alt="[Starting position];" style="float: center" width="50%"/>
<br>
<p class="para">
The starting position is illustrated above: the black ball is on the focus, with the red and the yellow on either side. 
<!--The cue ball is positioned anywhere on the line between the black and the pocket.-->
The first player is allowed to position the cue ball anywhere on the line between the black and the pocket.<br><br>

The first player names a colour (red or yellow) that he/she wants to pot and the first shot must hit that colour, or the shot is replayed. The other player must aim to pocket the other colour. <br><br>

The game proceeds like pool, with each player taking alternate shots. The first person to have potted both his/her colour and the black ball wins. A player who pots the black with his or her colour still on the table loses the game immediately.<br><br></p>
			
