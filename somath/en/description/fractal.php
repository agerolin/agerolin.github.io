									<h2>Fractals
					  <div style="float:right;"><a href="#finnish"><img src="../../images/iconinfinnish"  alt=""/></a></div></h2>
                  <p class="para">The term <em>"fractal"</em> was first used by <a href="https://www.encyclopedia.com/people/science-and-technology/mathematics-biographies/benoit-b-mandelbrot" target="new">Benoit Mandelbrot </a> in 1975. Mandelbrot based it on the Latin <em>frƒÅctus</em> meaning <em>"broken"</em> or <em>"fractured"</em>. Fractals extends the concept of theoretical fractional dimensions to geometric patterns in nature.<br><br>
                  In mathematics there is no precise definition of fractals. The general consensus is that theoretical fractals are infinitely self-similar, iterated, and detailed mathematical constructs having fractional dimensions, a generalised notion of euclidean dimension that can be applied to such objects. Fractals are of particular relevance in the field of chaos theory, since the graphs of meaningful chaotic processes are fractals.<br><br>

                  Both paintings in our exhibition aims to illustrate some of the above mentioned properties of Fractals. These fractals are a <a href="https://en.wikipedia.org/wiki/Julia_set"> Julia set </a> and a <a href="https://en.wikipedia.org/wiki/Menger_sponge">3D Menger sponge</a>. Notice that while zooming a Julia set you will find a Mandelbrot set (see picture below) in a sufficiently small portion of the painting.</p> <br><br>

                  <img src="../../images/fractals_mandelbrotset.png" alt="[Starting position];" style="float: center" width="50%"/><br>



                 <p class="para"> The amount of information available on the literature and web on fractals is endless. Our intention is just to give a starting point and to include a non-exaustive literature list for diving in the fascinating world of fractals.</p>


                  <h3> Fractals and Geometry </h3>

                  <p class="para">The concept of fractals has been inspiring many artistic works and has direct applications, for example, in the game industry to generate object shapes present in nature. <a href="https://en.wikipedia.org/wiki/Geometric_measure_theory">Geometric Measure Theory</a>, a branch of mathematics to study the geometric properties of sets - extending the euclidian notions of lines, surfaces, volume and dimension - provides a framework to study properties of fractals. In many mathematical theories, fractals are used as examples and counterexamples to illustrate the generality and consistency of concepts that are created.</p> <br><br></p><br><br><br><br>
			
