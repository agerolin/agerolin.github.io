					<p class="para">    <p>In our Magnetic <a href="https://en.wikipedia.org/wiki/Pendulum_(mathematics)" title="A pendulum is a weight suspended from a pivot so that it can swing freely. When a pendulum is displaced sideways from its resting, equilibrium position, it is subject to a restoring force due to gravity that will accelerate it back toward the equilibrium position." target="_new">pendulum</a> a metallic ball X is attached on a string, which is fixed on a pivot (top of pendulum base). Additionally, three magnets - denoted by R, G and B - are placed on the pendulum base in the bottom in such a way they form an equilateral triangle.  <br><br>

If we start the pendulum at various initial positions (out of its equilibrium position), it will swing according to the net force produced by the gravity and the magnetic attraction.  <br><br>

Due to friction and induced currents, pendulum will lose steadily its kinetic energy as it swings and the swinging will slow down. The mass of the ball X is designed light enough so that in the end X will converge on the vicinity of one of the magnets, i.e., the magnetic force dominates over the gravity. <br><br>
