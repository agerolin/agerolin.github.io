Feel free to play with our magnetic pendulum (or its <a onClick="myFunction()" style="cursor: pointer; cursor: hand;"> virtual version</a>). You will notice that depending on the initial position of the ball, it may end on top of different magnets. In fact, unless we start in immediate vicinity of one of the magnets, the tiniest change of the initial position of the pendulum can cause the ball X to take a totally different path and end on top of a different magnet. <br><br>

We are interested in the following <b>question</b>: <br>

<em>Given an initial position for the ball, can one predict on which magnets its going to end up?</em><br><br>

If you make more experiments and observe them more carefully, you get the intuition that we understand and can predict the movement of the pendulum in the next few seconds the ball is left from its initial position. Instead, it can be very hard to predict the trajectory for longer times and even to guess on which of the magnets R, G or B the ball may end up. <br><Br>

