					<h3>Do you know that...</h3>
					  <ul class="archive-grid">
						  <p class="para">						    The European Researchers' Night takes place yearly, typically starting on the last Friday of the month of September, and is the occasion for a Europe-wide public and media event for the promotion of research careers. <br>
							<br>
						  The aims of that event is to bring researchers closer to the general public and to increase awareness of research and innovation activities, with a view to supporting the public recognition of researchers, creating an understanding of the impact of researchers' work on citizen’s daily life, and encouraging young people to embark on research careers. <br><br>                         </p>

